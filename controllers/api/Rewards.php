class Rewards extends REST_Controller {

    function __construct()
    {
        // Construct the parent class
        parent::__construct();
        $this->load->model('rewards_model');
    }


    function ascendis_breakdown_post(){

        $data = $this->rewards_model->get_spend_breakdown('supps365');

        $message = [
            'success' => true, // Automatically generated by the model
            'data' => $data,
            'message' => 'OK'
        ];

        $this->set_response($message, REST_Controller::HTTP_CREATED); // CREATED (201) being the HTTP response 

    }

}
